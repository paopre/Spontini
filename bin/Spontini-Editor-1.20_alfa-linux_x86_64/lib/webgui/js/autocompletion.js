// Register our custom Codemirror hint plugin.
CodeMirror.registerHelper('hint', 'lilypondhint', function(editor) {
  var cur = editor.getCursor();
  var curLine = editor.getLine(cur.line);
  var start = cur.ch;
  var end = start;
  while (end < curLine.length && /^[\w$_]*$/.test(curLine.charAt(end))) ++end;
  while (start && ( (curLine.charAt(start - 1) == '\\') ||
                    (curLine.charAt(start - 1) == '\<') ||
                    /^[\w$_]*$/.test(curLine.charAt(start - 1)) ))
    --start
  var curWord = start !== end && curLine.slice(start, end)
  curWord = curWord.toString().replace('\\', '\\\\')
  var regex = new RegExp('^' + curWord, 'i');
  return {
      list: (!curWord ? [] : dictionary.filter(function(item) {
          return item.match(regex);
      })).sort(),
      from: CodeMirror.Pos(cur.line, start),
      to: CodeMirror.Pos(cur.line, end)
  }
});

CodeMirror.commands.autocomplete = function(cm) {
    CodeMirror.showHint(cm, CodeMirror.hint.lilypondhint, {completeSingle: false})
}
